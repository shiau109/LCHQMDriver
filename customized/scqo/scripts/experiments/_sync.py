"""Regenerate one launcher stub per cataloged experiment (manager tool).

    python scripts/experiments/_sync.py

Run this after promoting/registering a new experiment so it appears here as a
directly runnable script. Stubs are AUTO-GENERATED — never edit them by hand.
"""

from __future__ import annotations

import sys
from pathlib import Path

HERE = Path(__file__).resolve().parent
sys.path.insert(0, str(HERE.parent))  # scripts/ — for _lab (which registers experiments)

from _lab import build_session  # noqa: F401  (import side effect: registers experiments)
from scqo import catalog

TEMPLATE = '''"""Run {name} directly (AUTO-GENERATED by _sync.py — regenerate, never edit).

{description}

    python scripts/experiments/{name}.py --qubits q0 q1 --tag mytag
    python scripts/experiments/{name}.py --help        # full parameter list
"""

import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

from _cli import run_experiment_cli

if __name__ == "__main__":
    sys.exit(run_experiment_cli("{name}", doc=__doc__))
'''


def main() -> None:
    generated = []
    for entry in catalog():
        path = HERE / f"{entry['name']}.py"
        path.write_text(
            TEMPLATE.format(name=entry["name"], description=entry["description"]), encoding="utf-8"
        )
        generated.append(entry["name"])
    print(f"generated {len(generated)} launcher(s): {', '.join(sorted(generated))}")


if __name__ == "__main__":
    main()
